enum RoleHint {
  primary, // Ejercicios base del bloque (multiarticulares, alta carga)
  secondary, // Complementarios multiarticulares, soporte al primario
  accessory, // Aislamiento, control de fatiga, refinamiento
  stability, // Core, anti-rotación, control motor, articulaciones
  stretch, // Énfasis en rango alargado (glúteo/pierna/pectoral)
  shortened, // Énfasis en rango acortado (glúteo, deltoides, bíceps)
}
